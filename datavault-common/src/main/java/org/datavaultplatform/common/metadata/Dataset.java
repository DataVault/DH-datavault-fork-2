package org.datavaultplatform.common.metadata;

public class Dataset {
    
    private String id;
    private String name;

    public Dataset() {}

    public String getID() {
        return id;
    }

    public void setID(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}